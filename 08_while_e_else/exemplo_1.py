""" Iteração, Laços Condicionais e Loop. """

#  #### Exemplo 1 ####

# Variável (x) inicializada com valor (0)
x = 0
print("Executando a instrução while")
#   Enquanto a condição for VERDADEIRA, executa o loop
while x < 10:
    print(x)
    x += 1
# Após executar o loop, executa o "else"
else:
    print("Executanto a instrução else")
print("Fim")
