""" Trabalhando com listas. """

lista1 = ["Python"]
print(lista1)
print(type(lista1))

lista2 = list("Python")
print(lista2)
print(type(lista2))

lista3 = list(("Python",))
print(lista3)
print(type(lista3))

lista4 = list(("Python", "Estudar", 10, 3,))
print(lista4)
print(type(lista4))
