""" Funções: Parâmetros x Argumentos. """


# definindo uma função
# com "dois parâmetros"
def soma(num1, num2):
    """
    Esta função soma dois numeros
    e imprime o resultado
    """
    resultado = num1 + num2
    print(f"Soma de {num1} + {num2} = {resultado}")


# chamando a função "soma()"
# passando os argumentos "2 e 5git add ."
soma(2, 5)
