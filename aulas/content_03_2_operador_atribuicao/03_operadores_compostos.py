""" Operadores de Atribuição Compostos. """

# Inicializando as variáveis X e Y
x = 9
y = 3
# Agora vamos fazer alguns cálculos utilizando
# Atribuidores Compostos

x += y
print(x)
# Resetando variavel para valor original (9)
x = 9

x -= y
print(x)
# Resetando variavel para valor original (9)
x = 9

x *= y
print(x)
# Resetando variavel para valor original (9)
x = 9

x /= y
print(x)
# Resetando variavel para valor original (9)
x = 9

x %= y
print(x)
# Resetando variavel para valor original (9)
x = 9
