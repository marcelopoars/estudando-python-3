#!/usr/bin/env python3
# -*- coding: utf-8 -*-

""" Estudando Strings. """

s = "Para o Python, toda String é uma lista imutável."

# imprime o primeiro caracter da String
print(s[0])

# para o Python cada caracter de uma String é uma "String"
print(type(s[0]))

# imprime o último caracter da string
print(s[-1])

# fatiando a String
print(s[2:20])

# invertendo a String
print(s[:: -1])
