print("Este programa some dois números e mostra o resultado na tela.")
primeiro_numero = int(input("Digite o primeiro número: "))
segundo_numero = int(input("Digite o segundo número: "))
# Aqui eu crio uma variável que irá receber a soma dos dois números digitados
soma = primeiro_numero + segundo_numero
print("A soma de {} + {} é: {}" .format(primeiro_numero, segundo_numero, soma))
