""" Atribuição condicional - exemplo 01. """

x = 10
# A variável (texto) vai receber "Sim"
#  se a variável (x) for IGUAL a "10"
texto = "Sim" if x == 10 else "Não"
print(texto)


# Alterado o valor de x para 9
x = 9
# A variável (texto) vai receber "Sim"
#  se a variável (x) for IGUAL a "10"
texto = "Sim" if x == 10 else "Não"
print(texto)
